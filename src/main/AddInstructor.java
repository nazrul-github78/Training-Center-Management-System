    private void saveStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveStudentActionPerformed
        getTeacherValue();
        writeinTarea();
        if (tId.getSelectedIndex() < 0 && !vName.isEmpty() && !vAddress.isEmpty() && !vSubject.isEmpty() && !vContact.isEmpty() && !vDate.isEmpty() && (tMale.isSelected() || tFemale.isSelected())) {
            DataController.addTeacher(vId, vName, vGender, vAddress, vSubject, vContact, vDate);
            JOptionPane.showMessageDialog(null, "Teacher Saved Successfully! Please Generate a ID...");
            DataController.loadTeachersId(tId);
        } else {
            JOptionPane.showMessageDialog(null, "Please Give All Information Properly!!");
        }


    }//GEN-LAST:event_saveStudentActionPerformed

