    private void getTeacherValue() {
        vId = tId.getSelectedItem().toString();
        vName = tName.getText();
        if (tMale.isSelected()) {
            vGender = "Male";
        } else {
            vGender = "Female";
        }
        vAddress = tAddress.getText();
        vSubject = tSubject.getText();
        vContact = tContact.getText();
        vDate = tDate.getText();
    }

